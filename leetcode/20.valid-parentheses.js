/**
 * @param {string} s
 * @return {boolean}
 */
var isValid = function (s) {
  map = {
    '{': '}',
    '[': ']',
    '(': ')'
  };
  let stack = [],
    index = -1;
  for (let i = 0; i < s.length; i++) {
    stack.push(s[i])
    if (map[stack[stack.length - 2]] === s[i]) {
      stack.pop();
      stack.pop();
    }
  }
  if (stack.length === 0) {
    return true;
  } else {
    return false;
  }
};
